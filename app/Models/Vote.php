class Vote extends Model implements Auditable
{
    use LoadDefaults;
    use \OwenIt\Auditing\Auditable;
    use HasFactory;

     public $timestamps = true;
    public $table = 'votes';

    public $fillable = [
        'user_id',
        'proposal_id'
    ];

    protected $casts = [

    ];

    public static function rules(): array
    {
        return [
            'user_id' => 'required',
        'proposal_id' => 'required',
        'created_at' => 'nullable',
        'updated_at' => 'nullable'
        ];
    }

    /**
